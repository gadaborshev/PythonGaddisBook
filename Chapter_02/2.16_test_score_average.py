# Get three school grades and assign them to variables
# test1 test2 test3
test1 = float(input('Enter the first grade: '))
test2 = float(input('Enter the second grade: '))
test3 = float(input('Enter the third grade: '))

# calculate the average score from three grades and assign result to variable average
average = (test1 + test2 + test3) / 3.0

# show variable average
print('The average grade is', average)